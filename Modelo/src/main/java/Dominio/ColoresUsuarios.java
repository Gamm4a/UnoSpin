 class ColoresUsuarios {
    String colorParaRojo;
    String colorParaVerde;
    String colorParaAzul;
    String colorParaAmarillo;

    public ColoresUsuarios(String colorParaRojo, String colorParaVerde, String colorParaAzul, String colorParaAmarillo) {
        this.colorParaRojo = colorParaRojo;
        this.colorParaVerde = colorParaVerde;
        this.colorParaAzul = colorParaAzul;
        this.colorParaAmarillo = colorParaAmarillo;
    }

    public String getColorParaRojo() {
        return colorParaRojo;
    }

    public void setColorParaRojo(String colorParaRojo) {
        this.colorParaRojo = colorParaRojo;
    }

    public String getColorParaVerde() {
        return colorParaVerde;
    }

    public void setColorParaVerde(String colorParaVerde) {
        this.colorParaVerde = colorParaVerde;
    }

    public String getColorParaAzul() {
        return colorParaAzul;
    }

    public void setColorParaAzul(String colorParaAzul) {
        this.colorParaAzul = colorParaAzul;
    }

    public String getColorParaAmarillo() {
        return colorParaAmarillo;
    }

    public void setColorParaAmarillo(String colorParaAmarillo) {
        this.colorParaAmarillo = colorParaAmarillo;
    }
    
    
    
    
    
}
